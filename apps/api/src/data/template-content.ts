export type TemplateType = keyof typeof templateContents;
export type TemplateLanguage = 'en' | 'ar';

export interface TemplateContent {
  en: string;
  ar: string;
}

/**
 * Get template content for a specific template type and language
 */
export function getTemplateContent(
  templateType: TemplateType,
  language: TemplateLanguage = 'en'
): string {
  return templateContents[templateType][language];
}

/**
 * Get all available template types
 */
export function getAvailableTemplateTypes(): TemplateType[] {
  return Object.keys(templateContents) as TemplateType[];
}

/**
 * Check if a template type exists
 */
export function isValidTemplateType(type: string): type is TemplateType {
  return type in templateContents;
}

/**
 * Replace variables in template content
 */
export function replaceTemplateVariables(
  content: string,
  variables: Record<string, string>
): string {
  let result = content;

  for (const [key, value] of Object.entries(variables)) {
    const regex = new RegExp(`{{${key}}}`, 'g');
    result = result.replace(regex, value);
  }

  return result;
}

/**
 * Extract all variable names from a template
 */
export function extractTemplateVariables(content: string): string[] {
  const regex = /{{(\w+)}}/g;
  const variables: string[] = [];
  let match;

  while ((match = regex.exec(content)) !== null) {
    if (!variables.includes(match[1])) {
      variables.push(match[1]);
    }
  }

  return variables;
}
